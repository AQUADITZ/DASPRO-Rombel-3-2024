// Sekarang, Pak Dengklek ingin sebuah program yang keluarannya adalah:

// Pak Dengklek memiliki 75 ekor bebek.
// Terdapat tanda titik-titik (...) pada program di sebelah kanan. Isilah dengan sebuah bilangan agar keluaran di atas tercetak.

#include <iostream>

int main() {
    std::cout << "Pak Dengklek memiliki " << 75 << " ekor bebek." << std::endl;
}

// Pada C++, nilai "75" adalah string, sedangkan nilai 75 adalah bilangan (bulat).

// Jika dicetak, keduanya menghasilkan keluaran yang sama, yakni teks 75.

// Akan tetapi, nilai berupa bilangan memungkinkan kita untuk melakukan perhitungan ekspresi aritmetika, seperti yang akan dibahas pada soal berikutnya.
